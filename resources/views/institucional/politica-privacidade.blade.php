        <h1>Política de Privacidade - nativeapi_ofcluiz</h1>

        <p>
            Esta Política de Privacidade descreve como coletamos, usamos e protegemos as informações
            relacionadas ao uso do aplicativo NativeAPI e dos serviços de integração com a API do WhatsApp Business.
        </p>

        <h2>Informações coletadas</h2>
        <ul>
            <li>Dados fornecidos diretamente pelo usuário (nome, e-mail, número de telefone).</li>
            <li>Dados de uso dos serviços (mensagens via API, registros técnicos de integração).</li>
            <li>Informações necessárias para autenticação e autorização em conformidade com as políticas da Meta.</li>
        </ul>

        <h2>Uso das informações</h2>
        <p>As informações coletadas são utilizadas para:</p>
        <ul>
            <li>Habilitar o envio e recebimento de mensagens via WhatsApp Business API.</li>
            <li>Autenticação, suporte técnico e melhoria contínua do serviço.</li>
            <li>Cumprimento de exigências legais e regulatórias.</li>
        </ul>

        <h2>Compartilhamento de dados</h2>
        <p>Nunca vendemos informações pessoais. O compartilhamento ocorre somente com:</p>
        <ul>
            <li>Meta/WhatsApp, para funcionamento da API.</li>
            <li>Parceiros técnicos estritamente necessários para a prestação do serviço.</li>
            <li>Autoridades legais, quando exigido por lei.</li>
        </ul>

        <h2>Segurança</h2>
        <p>
            Adotamos medidas de segurança técnicas e organizacionais para proteger os dados,
            como criptografia e controle de acesso restrito.
        </p>

        <h2>Direitos do usuário</h2>
        <p>O usuário pode solicitar:</p>
        <ul>
            <li>Acesso, correção ou exclusão dos seus dados.</li>
            <li>Revogação do consentimento para uso do serviço.</li>
        </ul>

